class Practice16 {
  public static void main(String args[]) {
    char grade = 'n';
    switch (grade) {
      case 'a':
        System.out.println("fcd");
        break;
      case 'b':
        System.out.println("fc");
        break;
      case 'c':
        System.out.println("sc");
        break;
      case 'd':
        System.out.println("pass");
        break;
      case 'e':
        System.out.println("fail");
        break;
      default:
        System.out.println("invalid grade");
    }
  }
}